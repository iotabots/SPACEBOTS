var functionCallbacks = [];
var lockingFunction = function (callback) {
  if (locked) {
    functionCallbacks.push(callback);
  } else {
    $.longRunning(function (response) {
      while (functionCallbacks.length) {
        var thisCallback = functionCallbacks.pop();
        thisCallback(response);
      }
    });
  }
}

const applyAccessFor = function (address) {


  // what MetaMask injects as window.ethereum into each page
  console.log("window.ethereum", window.ethereum)

  if (window.ethereum == undefined) {
    console.log("Please install MetaMask!")
    lock()
    return
  }

  const provider = new ethers.providers.Web3Provider(window.ethereum)
  console.log("provider", provider)

  try {



    provider.getNetwork().then((network) => {

      console.log("chainId is:", network.chainId)
      if (network.chainId !== 148) {
        alert("Please connect to ShimmerEVM Mainnet");
        lock()
      }
    }).catch((error) => {
      console.log("error getNetwork:", error)
      lock()
    })

    provider.send("eth_requestAccounts", []).then((account) => {

      // The MetaMask plugin also allows signing transactions to
      // send ether and pay to change state within the blockchain.
      // For this, you need the account signer...
      const signer = provider.getSigner()
      console.log("signer", signer)
      signer.getAddress().then((account) => {

        console.log("account", account)

        let contract = new ethers.Contract(
          address,
          SPACEBOTS_ABI,
          provider
        )
        console.log("contract", contract)


        contract.walletOfOwner(account).then((data) => {

          console.log("data:", data)
          if (data.length <= 0) {
            console.log("No NFT Found! :-(")
            lock()
            return
          } else {
            console.log("Success! :-)")
            locked = false
            const items = data.map((i) => {
              let token_index = i.toNumber()
              console.log("token_index:", token_index)

              contract.tokenURI(token_index).then((metadata_url) => {
                console.log("metadata_url:", metadata_url)

                axios.get(metadata_url).then((metadata) => {

                  console.log("metadata:", metadata.data)
                  console.log("items:", items)
                  return metadata.data
                })
              })

            })
          }

        }).catch((error) => {
          console.log("error getAddress:", error)
          lock()
        })
      }).catch((error) => {
        console.log("error getAddress:", error)
        lock()
      })
    }).catch((error) => {
      console.log("error eth_requestAccounts:", error)
      lock()
    })


  } catch (error) {
    console.log("Something wrent wrong!")
    lock()
  }
}

